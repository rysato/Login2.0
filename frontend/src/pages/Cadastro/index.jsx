import React, { useRef, useState } from 'react';
import { useNavigate } from 'react-router-dom';
import api from '../../services/api';

function Cadastro() {
    const navigate = useNavigate();
    const [showPass, setShowPass] = useState(false);
    const refs = { name: useRef(), age: useRef(), email: useRef(), password: useRef() };

    async function handleRegister(e) {
        e.preventDefault();
        try {
            await api.post('/usuarios', {
                name: refs.name.current.value,
                age: refs.age.current.value,
                email: refs.email.current.value,
                password: refs.password.current.value
            });
            alert("Cadastro realizado com sucesso!");
            navigate('/');
        } catch (err) {
            alert(err.response?.data?.error || "Erro ao cadastrar");
        }
    }

    return (
        <div className="container">
            <form onSubmit={handleRegister}>
                <h1>Criar Conta</h1>
                <input placeholder="Nome Completo" ref={refs.name} required />
                <input placeholder="Idade" type="number" ref={refs.age} required />
                <input placeholder="E-mail" type="email" ref={refs.email} required />
                
                <div style={{position: 'relative', width: '100%'}}>
                    <input 
                        placeholder="Senha" 
                        type={showPass ? "text" : "password"} 
                        ref={refs.password} 
                        required 
                    />
                    <button 
                        type="button" 
                        onClick={() => setShowPass(!showPass)} 
                        style={{
                            position: 'absolute', right: '15px', top: '50%', transform: 'translateY(-50%)', 
                            background: 'none', border: 'none', width: 'auto', padding: 0, cursor: 'pointer', color: '#666'
                        }}
                    >
                        {showPass ? (
                             <svg xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M17.94 17.94A10.07 10.07 0 0 1 12 20c-7 0-11-8-11-8a18.45 18.45 0 0 1 5.06-5.94M9.9 4.24A9.12 9.12 0 0 1 12 4c7 0 11 8 11 8a18.5 18.5 0 0 1-2.16 3.19m-6.72-1.07a3 3 0 1 1-4.24-4.24"></path><line x1="1" y1="1" x2="23" y2="23"></line></svg>
                        ) : (
                            <svg xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z"></path><circle cx="12" cy="12" r="3"></circle></svg>
                        )}
                    </button>
                </div>

                <button type="submit" className="primary-btn">Cadastrar</button>
                <button type="button" className="secondary-btn" onClick={() => navigate('/')}>JÃ¡ tenho uma conta</button>
            </form>
        </div>
    );
}
export default Cadastro;